    private void button_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_searchActionPerformed
        // TODO add your handling code here:
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/optical_store_management_system", "root", "");
            Statement stm = con.createStatement();
            search = setkeyword();
            
            query = "SELECT * from employee_information WHERE employee_id = '" + search + "'";
            rs = stm.executeQuery(query);
            
            if (rs.next()){
                id = rs.getString("employee_id");
                name = rs.getString("employee_name");
                phone = rs.getString("employee_phone_num");
                address = rs.getString("employee_address");
                desig = rs.getString("designation");
                
                label_id_rec.setText(id);
                label_id_rec.setVisible(true);
                textbox_name.setText(name);
                textbox_phone.setText(phone);
                textbox_address.setText(address);
                textbox_desig.setText(desig);
            }
            
            con.close();
            
        }
        catch (SQLException sqlExp) {
            JOptionPane.showMessageDialog(null, sqlExp);
        }
    }//GEN-LAST:event_button_searchActionPerformed

    private void button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_updateActionPerformed
        // TODO add your handling code here:
        try {
            name = textbox_name.getText();
            phone = textbox_phone.getText();
            address = textbox_address.getText();
            desig = textbox_desig.getText();
            search = setkeyword();
            
            if (desig.equals("") || name.equals(""))
                JOptionPane.showMessageDialog(null, "NAME/DESIGNATION CANNOT BE EMPTY");
            
            else
            {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/optical_store_management_system", "root", "");
                Statement stm = con.createStatement();
                stm.execute("SET FOREIGN_KEY_CHECKS=0");
            
                query = "UPDATE `employee_information` SET `employee_name` = '" + name + "',`employee_phone_num` = '" + phone + "',`employee_address` = '" + address + "',`designation`= '" + desig + "' WHERE employee_id = '" + search + "'";
                stm.executeUpdate(query);
            
                stm.execute("SET FOREIGN_KEY_CHECKS=1");            
                con.close();
            
                JOptionPane.showMessageDialog(null, "CHANGES SAVED");
            }
        }
        catch (SQLException sqlExp) {
            JOptionPane.showMessageDialog(null, sqlExp);
        }
    }//GEN-LAST:event_button_updateActionPerformed

    private void button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_deleteActionPerformed
        // TODO add your handling code here:
        int delcon = JOptionPane.showConfirmDialog(null,"ARE YOU SURE?"); 
        
        if(delcon == JOptionPane.YES_OPTION){
            try{
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/optical_store_management_system", "root", "");
                Statement stm = con.createStatement();
                stm.execute("SET FOREIGN_KEY_CHECKS=0");
                search = setkeyword();

                query = "DELETE FROM `employee_information` WHERE employee_id = '" + search + "'";
                stm.executeUpdate(query);

                query = "DELETE FROM `employee_permissions` WHERE employee_id = '" + search + "'";
                stm.executeUpdate(query);

                query = "DELETE FROM `login_info` WHERE employee_id = '" + search + "'";
                stm.executeUpdate(query);

                query = "UPDATE sales_records SET employee_id = '' WHERE employee_id = '" + search + "'";
                stm.executeUpdate(query);

                stm.execute("SET FOREIGN_KEY_CHECKS=1");            
                con.close();

                JOptionPane.showMessageDialog(null, "EMPLOYEE REMOVED FROM STORE DATABASE");

                employee_info main = new employee_info(ob);
                main.setVisible(true);
                this.dispose();
            }
            catch (SQLException sqlExp) {
                JOptionPane.showMessageDialog(null, sqlExp);
            }
        }
        
    }//GEN-LAST:event_button_deleteActionPerformed

