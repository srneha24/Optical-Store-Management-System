    String setkeyword()
    {
        String choice = combobox_search.getSelectedItem().toString();
        
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/optical_store_management_system", "root", "");
            Statement stm = con.createStatement();
            
            try {
                if (choice.compareTo("ID") == 0)
                query = "SELECT employee_id from employee_information WHERE employee_id = '" + textfield_search.getText() + "'";
                
                if(choice.compareTo("Name") == 0)
                    query = "SELECT employee_id from employee_information WHERE employee_name = '" + textfield_search.getText() + "'";
                
                rs = stm.executeQuery(query);
            }
            catch (SQLException sqlExp) {
                String msg = "'" + choice + "' IS NOT INCLUDED IN THE DATABASE";
                JOptionPane.showMessageDialog(null, msg);
            }
            
            if(rs.next()) {
                keyword = rs.getString("employee_id");
                
                con.close();
            }
        }
        catch (SQLException sqlExp) {
            JOptionPane.showMessageDialog(null, sqlExp);
        }
        
        return keyword;
    }
    

